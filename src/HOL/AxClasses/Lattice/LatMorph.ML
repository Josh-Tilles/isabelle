

(** monotone functions vs. "&&"- / "||"-semi-morphisms **)

Goalw [is_mono_def] "is_mono f = (ALL x y. f (x && y) [= f x && f y)";
  by Safe_tac;
  (*==> (level 1)*)
    by (stac le_inf_eq 1);
    by (rtac conjI 1);
    by (Step_tac 1);
    by (Step_tac 1);
    by (etac mp 1);
    by (rtac inf_lb1 1);
    by (Step_tac 1);
    by (Step_tac 1);
    by (etac mp 1);
    by (rtac inf_lb2 1);
  (*==> (level 11)*)
    by (rtac (conjI RS (le_trans RS mp)) 1);
    by (rtac inf_lb2 2);
    by (subgoal_tac "x && y = x" 1);
    by (etac subst 1);
    by (Fast_tac 1);
    by (stac inf_connect 1);
    by (assume_tac 1);
qed "mono_inf_eq";

Goalw [is_mono_def] "is_mono f = (ALL x y. f x || f y [= f (x || y))";
  by Safe_tac;
  (*==> (level 1)*)
    by (stac ge_sup_eq 1);
    by (rtac conjI 1);
    by (Step_tac 1);
    by (Step_tac 1);
    by (etac mp 1);
    by (rtac sup_ub1 1);
    by (Step_tac 1);
    by (Step_tac 1);
    by (etac mp 1);
    by (rtac sup_ub2 1);
  (*==> (level 11)*)
    by (rtac (conjI RS (le_trans RS mp)) 1);
    by (rtac sup_ub1 1);
    by (subgoal_tac "x || y = y" 1);
    by (etac subst 1);
    by (Fast_tac 1);
    by (stac sup_connect 1);
    by (assume_tac 1);
qed "mono_sup_eq";


